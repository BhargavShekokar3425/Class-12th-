#Python code to demonstrate Implementing stack using a list #
stack=["One","Two","Three"]
stack.append("Four")
stack.append("Five")
print("Print the elements in the stack",stack)
#Remove the last item
print("pop1",stack.pop())
print("after performing first pop",stack)
#removes the last item
print("pop 2", stack.pop())
print("After performing second pop",stack)